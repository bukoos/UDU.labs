# Користувач вводить значення температури t у градусах Цельсія. 
# Напишіть програму, яка виводить назву стану, у якому знаходиться вода при цій температурі за нормальних умов.
# Отримати температуру від користувача Буртенко 31І
t = float(input("Введіть температуру у градусах Цельсія: "))

# Перевірка умов для визначення стану води
if t <= 0:
    print("Лід")
elif 0 < t < 100:
    print("Стан рідкий")
else:
    print("Пар")
